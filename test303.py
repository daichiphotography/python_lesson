#Decimal numbers conversion

a = 12
b = 1234567

print(f'a={a:,} b ={b:,}')

print(f'a ={a:09} b ={b:09}')
c = 123.4
d = 123.456789

print(f'c = {c:.3f} d = {d: .3f}')
print(f'c = {c: .5f} d = {d: .5f}')

#Binary and Hexadecimal

z = 123
x = 255
j = 65535

print(f'Binary z ={z:#b} x = {x:#b} j = {j:#b}')

